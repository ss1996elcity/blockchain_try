export function sum(x, y) {
  return x + y
}
export function difference(x, y) {
  return x - y
}
export function product(x, y) {
  return x * y
}
export function quotient(x, y) {
  return x / y
}

exports.sum = sum;
exports.difference = difference;
exports.product = product;
exports.quotient = quotient;